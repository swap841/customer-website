const DEFAULT_COORDS = { lat: 20.5937, lng: 78.9629, city: "India" };

export function pincodeToCoords(pincode: string): { lat: number; lng: number; city: string } {
  const clean = pincode.replace(/\s/g, "");
  if (clean.length !== 6 || !/^\d{6}$/.test(clean)) return DEFAULT_COORDS;

  // Try exact pincode match first
  if (PINCODE_COORDS[clean]) return PINCODE_COORDS[clean];

  // Try first 4 digits
  const first4 = clean.substring(0, 4);
  if (PINCODE_COORDS[first4]) return PINCODE_COORDS[first4];

  // Try first 3 digits
  const first3 = clean.substring(0, 3);
  if (PINCODE_COORDS[first3]) return PINCODE_COORDS[first3];

  return DEFAULT_COORDS;
}
